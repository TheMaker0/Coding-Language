# Tuples
t = (1, 2, 3, 4, 5)
print("Minimum value in tuple t:", min(t))

# Dictionaries
d = {"apple": 1, "banana": 2, "cherry": 3}
print("Minimum key in dictionary d:", min(d.keys()))
print("Minimum value in dictionary d:", min(d.values()))

# Lists
l = [6, 7, 8, 9, 10]
print("Minimum value in list l:", min(l))
