# Tuples
t = (1, 2, 3, 4, 5)
print("Maximum value in tuple t:", max(t))

# Dictionaries
d = {"apple": 1, "banana": 2, "cherry": 3}
print("Maximum key in dictionary d:", max(d.keys()))
print("Maximum value in dictionary d:", max(d.values()))

# Lists
l = [6, 7, 8, 9, 10]
print("Maximum value in list l:", max(l))
