# Tuples
t = (1, 2, 3, 4, 5)
print("Length of tuple t:", len(t))

# Dictionaries
d = {"apple": 1, "banana": 2, "cherry": 3}
print("Keys in dictionary d:", d.keys())
print("Values in dictionary d:", d.values())

# Lists
l = [6, 7, 8, 9, 10]
print("Length of list l:", len(l))
print("Last element of list l:", l[-1])
